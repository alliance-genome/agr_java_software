package org.alliancegenome.neo4j.entity.node;

import java.util.Date;
import java.util.List;

import org.alliancegenome.es.util.DateConverter;
import org.alliancegenome.neo4j.entity.Neo4jEntity;
import org.alliancegenome.neo4j.view.View;
import org.eclipse.microprofile.openapi.annotations.media.Schema;
import org.neo4j.ogm.annotation.NodeEntity;
import org.neo4j.ogm.annotation.typeconversion.Convert;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonView;

import lombok.Getter;
import lombok.Setter;

@Getter
@Setter
@NodeEntity
@Schema(name = "OntologyFileMetadata", description = "POJO that represents the Ontology File Metadata")
public class OntologyFileMetadata extends Neo4jEntity {

	@JsonView({ View.ReleaseInfo.class })
	private String defaultNamespace;
	@JsonView({ View.ReleaseInfo.class })
	private String owlAxioms;
	@JsonView({ View.ReleaseInfo.class })
	private String autoGeneratedBy;
	@JsonView({ View.ReleaseInfo.class })
	private String savedBy;
	@Convert(value = DateConverter.class)
	@JsonProperty(value = "releaseDate")
	private Date date;
	@JsonView({ View.ReleaseInfo.class })
	@JsonProperty(value = "releaseVersion")
	private String formatVersion;

	// no need for now, may put into different view later
	private List<String> remark;
	private List<String> synonymtypedef;
	private List<String> property_value;
	private List<String> treatXrefsAsGenusDifferentia;
	private List<String> ontology;
	private List<String> subsetdef;

}
